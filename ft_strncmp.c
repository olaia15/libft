#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t num)
{
	while (num-- && *s1 && *s2)
	{
		if (*s1 != *s2)
		{
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		}
		s1++;
		s2++;
	}
	if (num == (size_t)-1)
		return (0);
	else
		return (*(unsigned char *)s1 - *(unsigned char *)s2);
}
